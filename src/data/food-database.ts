// Função para buscar alimentos por nome
export const searchFood = (query: string): FoodItem[] => {
  const searchTerm = query.toLowerCase();
  return Object.values(foodDatabase).filter(food => 
    food.name.toLowerCase().includes(searchTerm) ||
    food.brazilianName?.toLowerCase().includes(searchTerm) ||
    food.category.toLowerCase().includes(searchTerm) ||
    food.subcategory.toLowerCase().includes(searchTerm)
  );
};

// Função para buscar por categoria
export const getFoodsByCategory = (category: string): FoodItem[] => {
  return Object.values(foodDatabase).filter(food => 
    food.category.toLowerCase() === category.toLowerCase()
  );
};

// Função para buscar por subcategoria
export const getFoodsBySubcategory = (subcategory: string): FoodItem[] => {
  return Object.values(foodDatabase).filter(food => 
    food.subcategory.toLowerCase() === subcategory.toLowerCase()
  );
};

// Função para obter categorias disponíveis
export const getCategories = (): string[] => {
  const categories = new Set(Object.values(foodDatabase).map(food => food.category));
  return Array.from(categories).sort();
};

// Função para obter subcategorias
export const getSubcategories = (): string[] => {
  const subcategories = new Set(Object.values(foodDatabase).map(food => food.subcategory));
  return Array.from(subcategories).sort();
};

// Função para obter alimentos saudáveis (score > 80)
export const getHealthyFoods = (): FoodItem[] => {
  return Object.values(foodDatabase).filter(food => food.healthScore > 80);
};

// Função para obter alimentos por faixa de calorias
export const getFoodsByCalorieRange = (min: number, max: number): FoodItem[] => {
  return Object.values(foodDatabase).filter(food => 
    food.calories >= min && food.calories <= max
  );
}; 